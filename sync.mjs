// Inject a structured course map into each overview, replacing the old inline
// "Start here" sentence links. Built from what was actually published, so it
// cannot list a page that doesn't exist.
for (const c of Object.values(COURSES)) {
  const ovRel = c.overview + ".md"
  if (!written.has(ovRel)) continue
  const prefix = c.dir + "/"
  const items = [...written]
    .filter((rel) => rel.startsWith(prefix) && rel !== ovRel)
    .map((rel) => ({
      rel: rel.slice(prefix.length),
      title: pageTitles.get(rel) ?? rel.split("/").pop().replace(/\.md$/, ""),
    }))
  const map = courseMapHtml(items, c.dir)
  if (!map) continue
  let ov = await readFile(join(OUT, ovRel), "utf8")
  // The old navigation: a "> **Start here:** …" blockquote of inline links.
  ov = ov.replace(/^> \*\*Start here:\*\*[^\n]*\n+/m, "")
  // Insert before the first H2 — after the intro prose, before the content.
  ov = /^## /m.test(ov) ? ov.replace(/^## /m, `${map}## `) : ov + "\n" + map
  await writeFile(join(OUT, ovRel), ov)
}

// content/ sits under ~/Documents, which is cloud-synced. When this script clears
// the directory, the sync client restores what it just deleted as conflict copies
// ("index 2.md"), and those land in content/ *after* the filter has run — so they
// reach the site unchecked. That is a filter bypass, not just clutter: a conflict
// copy of a withheld page would be published.
//
// Deleting anything this run did not itself write makes content/ exactly the
// filter's output, whatever else tries to appear in it.
const strays = (await walk(OUT))
  .map((f) => relative(OUT, f))
  .filter((rel) => !written.has(rel))

for (const rel of strays) await rm(join(OUT, rel))
if (strays.length) {
  console.log(`Removed ${strays.length} stray file(s) not produced by this sync:`)
  for (const rel of strays.slice(0, 5)) console.log(`  ${rel}`)
  if (strays.length > 5) console.log(`  … and ${strays.length - 5} more`)
}

console.log(
  `Published ${published.length} of ${files.length} pages ` +
    `(${files.length - published.length} withheld, ${strippedCount} had sections stripped) ` +
    `+ the site landing page.`,
)
