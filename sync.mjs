// Matched as a prefix, so "## Gaps this material does *not* fill" is caught by "Gaps".
// Safe for this list: none of these names begins a student-facing heading.

const isDropped = (rel) =>
  DROP_PAGES.has(rel) || DROP_DIRS.some((d) => rel.includes(d))

async function walk(dir) {
  const out = []
  for (const entry of await readdir(dir, { withFileTypes: true })) {
    const full = join(dir, entry.name)
    if (entry.isDirectory()) out.push(...(await walk(full)))
    else if (entry.name.endsWith(".md")) out.push(full)
  }
  return out
}

// Remove an H2 section: the heading through to the next H2 or end of file.
function stripSections(body) {
  for (const name of STRIP_SECTIONS) {
    const esc = name.replace(/[.*+?^${}()|[\]\\]/g, "\\$&")
    // Terminate at the next H2 or end of file. JS has no \z, so the alternative
    // is a negative lookahead for any remaining character.
    body = body.replace(
      new RegExp(`^## ${esc}.*$[\\s\\S]*?(?=^## |(?![\\s\\S]))`, "gm"),
      "",
    )
  }
  return body
}

// Split a markdown table row into cells, respecting `\|` escapes — the registers
// carry `[[path\|alias]]` wikilinks whose pipe must not be read as a column break.
function splitRow(line) {
  const cells = line.split(/(?<!\\)\|/)
  if (cells.length && cells[0].trim() === "") cells.shift()
  if (cells.length && cells[cells.length - 1].trim() === "") cells.pop()
  return cells
}

// Remove named columns from every GitHub-style table in the text. Used to drop the
// internal "Announced" and "Gap" columns while keeping the student-facing ones.
function dropTableColumns(body, dropHeaders) {
  const lines = body.split("\n")
  const out = []
  for (let i = 0; i < lines.length; i++) {
    const sep = lines[i + 1]
    const isTableHead =
      lines[i].includes("|") &&
      sep &&
      /-/.test(sep) &&
      /^\s*\|?[\s:|-]+\|[\s:|-]*$/.test(sep)
    if (!isTableHead) {
      out.push(lines[i])
      continue
    }
    const drop = new Set(
      splitRow(lines[i])
        .map((h, idx) => (dropHeaders.includes(h.trim()) ? idx : -1))
        .filter((idx) => idx >= 0),
    )
    if (drop.size === 0) {
      out.push(lines[i])
      continue
    }
    let j = i
    while (j < lines.length && lines[j].includes("|") && lines[j].trim() !== "") {
      const kept = splitRow(lines[j]).filter((_, idx) => !drop.has(idx))
      out.push("| " + kept.map((c) => c.trim()).join(" | ") + " |")
      j++
    }
    i = j - 1
  }
  return out.join("\n")
}

